//----------------------------------------------------------------------------//
#define CLIP_MINMAX(MIN, MAX, IN)      \
  if      ((IN) < (MIN)) (IN) = (MIN); \
  else if ((IN) > (MAX)) (IN) = (MAX);

//----------------------------------------------------------------------------//
#define CLIP_MIN(MIN, IN)         \
  if ((IN) < (MIN)) (IN) = (MIN);

//----------------------------------------------------------------------------//
#define CLIP_MAX(MAX, IN)         \
  if ((IN) > (MAX)) (IN) = (MAX);
